package model.data;

public class Action implements Bean {
	
	private int id;
	private String name;
	
	public Action() {}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
